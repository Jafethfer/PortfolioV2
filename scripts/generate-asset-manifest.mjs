// Scans public/assets for preloadable media (images + audio) and writes a TS
// manifest the app loads on the landing screen to warm the cache. Runs on
// `prestart` / `prebuild` (see package.json) so it stays in sync as assets are
// added — and can be run directly via `npm run assets:manifest`.
//
// Output is GROUPED by stage, not one flat list: the landing screen blocks the
// Start button on only `core` + `stage-1`, then background-preloads the later
// stages over the same HTTP/2 connection. See `AssetPreloadService`.
//
// Excludes non-media files (e.g. sprite-sheet tooling JSON) — only assets the
// running app actually fetches belong here.
import { mkdirSync, readdirSync, statSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';

const PUBLIC_DIR = 'public';
const ASSETS_DIR = join(PUBLIC_DIR, 'assets');
const OUT_FILE = join('src', 'app', 'generated', 'asset-manifest.ts');
// GIFs are intentionally excluded — the pipeline converts them to animated
// WebP (see the Ryo stage); raw GIFs are heavy source files, not shipped assets.
const MEDIA = /\.(png|jpe?g|webp|avif|svg|mp3|wav|ogg)$/i;

// Ordered to mirror app.routes.ts (stage-1 = Terry, stage-2 = Joe, stage-3 =
// Ryo). Every asset a character owns lives under a path containing its name
// (`img/characters/<name>`, `img/stage/<name>-stage`, `sfx/<name>`, and the
// `<name>…-stage-ost` track), so a substring match buckets the whole set. An
// asset matching no character (misc/tech UI, landing/closing, shared SFX) is
// `core`. Adding a character = append its name here (and a route). Anything
// left unmatched safely falls back to `core`, i.e. preloads up front — the old
// behaviour — so a forgotten entry costs speed, never correctness.
const STAGES = [
  { key: 'stage-1', match: 'terry' },
  { key: 'stage-2', match: 'joe' },
  { key: 'stage-3', match: 'ryo' },
];

/** @param {string} dir @returns {string[]} absolute-ish file paths under dir */
function walk(dir) {
  const found = [];
  for (const name of readdirSync(dir)) {
    const full = join(dir, name);
    if (statSync(full).isDirectory()) found.push(...walk(full));
    else if (MEDIA.test(name)) found.push(full);
  }
  return found;
}

/** @param {string} url @returns {string} bucket key */
function bucketOf(url) {
  for (const { key, match } of STAGES) if (url.includes(match)) return key;
  return 'core';
}

// Paths served relative to <base href> — i.e. drop the `public/` prefix and use
// forward slashes so they match how the app references assets (`assets/...`).
const urls = walk(ASSETS_DIR)
  .map((f) => relative(PUBLIC_DIR, f).split('\\').join('/'))
  .sort();

const order = ['core', ...STAGES.map((s) => s.key)];
const groups = Object.fromEntries(order.map((k) => [k, []]));
for (const u of urls) groups[bucketOf(u)].push(u);

const body = order
  .map((k) => {
    const items = groups[k].map((u) => `    '${u}',`).join('\n');
    return items ? `  '${k}': [\n${items}\n  ],` : `  '${k}': [],`;
  })
  .join('\n');

const content = `// AUTO-GENERATED by scripts/generate-asset-manifest.mjs — do not edit by hand.
// Refresh with \`npm run assets:manifest\` (also runs on prestart / prebuild).
export const ASSET_GROUPS = {
${body}
} as const;

export type AssetGroup = keyof typeof ASSET_GROUPS;
`;

mkdirSync(dirname(OUT_FILE), { recursive: true });
writeFileSync(OUT_FILE, content);
const counts = order.map((k) => `${k}=${groups[k].length}`).join(' ');
console.log(`asset-manifest: ${urls.length} files (${counts}) -> ${OUT_FILE}`);

// Scans public/assets for preloadable media (images + audio) and writes a TS
// manifest the app loads on the landing screen to warm the cache. Runs on
// `prestart` / `prebuild` (see package.json) so it stays in sync as assets are
// added — and can be run directly via `npm run assets:manifest`.
//
// Excludes non-media files (e.g. sprite-sheet tooling JSON) — only assets the
// running app actually fetches belong here.
import { mkdirSync, readdirSync, statSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';

const PUBLIC_DIR = 'public';
const ASSETS_DIR = join(PUBLIC_DIR, 'assets');
const OUT_FILE = join('src', 'app', 'generated', 'asset-manifest.ts');
const MEDIA = /\.(png|jpe?g|webp|avif|gif|svg|mp3|wav|ogg)$/i;

/** @param {string} dir @returns {string[]} absolute-ish file paths under dir */
function walk(dir) {
  const found = [];
  for (const name of readdirSync(dir)) {
    const full = join(dir, name);
    if (statSync(full).isDirectory()) found.push(...walk(full));
    else if (MEDIA.test(name)) found.push(full);
  }
  return found;
}

// Paths served relative to <base href> — i.e. drop the `public/` prefix and use
// forward slashes so they match how the app references assets (`assets/...`).
const urls = walk(ASSETS_DIR)
  .map((f) => relative(PUBLIC_DIR, f).split('\\').join('/'))
  .sort();

const body = urls.map((u) => `  '${u}',`).join('\n');
const content = `// AUTO-GENERATED by scripts/generate-asset-manifest.mjs — do not edit by hand.
// Refresh with \`npm run assets:manifest\` (also runs on prestart / prebuild).
export const ASSET_MANIFEST: readonly string[] = [
${body}
];
`;

mkdirSync(dirname(OUT_FILE), { recursive: true });
writeFileSync(OUT_FILE, content);
console.log(`asset-manifest: ${urls.length} files -> ${OUT_FILE}`);
